# Cálculo da hipotenusa (c).
# Elevar a 0.5 é o mesmo que elevar a 1/2 ou a própria raiz do número.
a = 3.0
b = 4.0
c = (a ** 2 + b ** 2) ** 0.5
print(f'1) c = {c}\n')

